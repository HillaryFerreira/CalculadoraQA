public class Calculadora {
    
    /**
     *
     * @param num1 primeiro numero double que sera usado na soma
     * @param num2 segundo numero double que sera usado na soma
     * @return resultado de n1 mais n2
     */

    public double somar(double num1, double num2) {
        return num1 + num2;
    }

    /**
     *
     * @param n1 primeiro numero double que sera usado na subtracao
     * @param n2 segundo numero double que sera usado na subtracao
     * @return resultado de n1 menos n2
     */
    public double subtrair(double num1, double num2) {
        return num1 - num2;
    }
    
     /**
     *
     * @param n1 primeiro numero double que sera usado na multiplicacao
     * @param n2 segundo numero double que sera usado na multiplicacao
     * @return resultado de n1 multiplicado por n2
     */
    public double multiplicar(double num1, double num2) {
        return num1 * num2;
    }
    
    /**
     *
     * @param n1 primeiro numero double que sera usado na divisao
     * @param n2 segundo numero double que sera usado na divisao
     * @return resultado de n1 dividido por n2
     */
    public double dividir(double num1, double num2) {
        return num1 / num2;
    }
    
    
    
    
}
